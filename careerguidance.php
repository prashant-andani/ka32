					<h2> <i class="fa fa-puzzle-piece"></i>&nbsp;Aptitude Skills</h2>
					<p>Aptitudes are natural talents, special abilities for doing, or learning to do, certain kinds of things easily and quickly. They have little to do with knowledge or culture, or education, or even interests. 
						<br>Aptitude Skill tests are time based designed to measure individual work-related cognitive capacity. Apititude Skills test measure the inidividuals intelligence : 
						<li>ability to think and reason abstractly and solve problems</li>
						<li>ability to learn from past experiences and relevant learning, and to apply this learning to a situation </li>
						<br>
						<b>Types of Aptitude Tests :</b>
						<li>Abstract reasoning :Also called conceptual reasoning, this test is unique in its design. It is a non-verbal test, which uses shapes rather than words or text to measure someone’s fluid intelligence. Each test question includes a series of shapes with common logical rules.</li>
						<li>Verbal reasoning test : This is a timed test that is designed to measure your verbal analytical skills (or verbal reasoning skills). These skills include the capacity to quickly identify critical issues from written material such as reports, and logically derive conclusions from written facts or data</li>
						<li>Numerical Reasoning Test : This test is also a timed test. It measures your numerical analytical skills (or numerical reasoning skills).These skills include the capacity to quickly identify critical issues from numerical data such as graphs and tables. It also includes the capacity to use work-related numerical data such as performance figures or financial outcomes to make effective decisions.</li>
					
						<br>These Aptitude skill tests are of high importance for Job seekers after the graduation ranging from Engineering, Law, Banking and Accounting, Civil Service / Departmental Examinations and pursuing higher studies. Learn the aptitude skills in problem solving through this classroom based video.
